function oppositeNumber(number) {
    /**
     * A function that takes one argument, a number.
     * The function returns the opposite of the number.
     * @param {number} number
     * @return {number}
     * @example
     * oppositeNumber(1) // returns -1
     * oppositeNumber(-5) // returns 5
     */
    return -number;
}

console.log(oppositeNumber(1)); // -1
console.log(oppositeNumber(-5)); // 5
console.log(oppositeNumber(0)); // 0
